def generate_hexagram(home_team, away_team, date_str):
    """鍩轰簬姣旇禌淇℃伅鐢熸垚鍏埢鍗﹁薄锛堢‘瀹氭€х畻娉曪級"""
    seed = f"{home_team}{away_team}{date_str}".encode("utf-8")
    h = hashlib.sha256(seed).hexdigest()
    # 鐢╤ash鐢熸垚6鐖?(浠庝笅寰€涓?
    lines = []
    changing = []
    for i in range(6):
        v = int(h[i*2:i*2+2], 16) % 4  # 0-3
        if v == 0: lines.append(1); changing.append(0)   # 鑰侀槼 鈫?闃?
        elif v == 1: lines.append(1); changing.append(0)  # 灏戦槼
        elif v == 2: lines.append(0); changing.append(0)  # 灏戦槾
        else: lines.append(0); changing.append(1)         # 鑰侀槾 鈫?闃?
    
    # 涓诲崷 (鏈崷)
    hex_key = "".join(str(b) for b in lines)
    # 鍙樺崷 (涔嬪崷) - 鑰侀槼鍙橀槾, 鑰侀槾鍙橀槼
    changed = [1 - lines[i] if changing[i] else lines[i] for i in range(6)]
    changed_key = "".join(str(b) for b in changed)
    
    return {
        "lines": lines,           # 0=闃?1=闃? 浠庝笅寰€涓?
        "changing": changing,     # 1=鍙樼埢
        "hex_key": hex_key,
        "changed_key": changed_key,
    }

